package modproxy

import (
	"fmt"
	"os"
	"path/filepath"
	"testing"

	"github.com/stretchr/testify/require"
	"gotest.tools/v3/golden"
)

const fakeManifest = `# Auto-generated by goscrape - do not edit manually
{
  version = "%s";
  date = "2024-01-01";
  module = "example.com/tool";
  subPackages = ["cmd/tool"];
  go = "%s";
  license = "%s";
  hash = "sha256-fake=";

  mod = {
    "example.com/dep" = {
      version = "v1.0.0";
      hash = "sha256-fake=";
      go = "1.10";
      packages = ["example.com/dep"];
    };
  };
}
`

// olderFakeManifest predates the license field.
const olderFakeManifest = `# Auto-generated by goscrapeproxy - do not edit manually
{
  version = "%s";
  date = "2024-01-01";
  module = "example.com/tool";
  subPackages = ["cmd/tool"];
  go = "%s";
  hash = "sha256-fake=";

  mod = {
    "example.com/dep" = {
      version = "v1.0.0";
      hash = "sha256-fake=";
      go = "1.10";
      packages = ["example.com/dep"];
    };
  };
}
`

func TestWriteIndex(t *testing.T) {
	dir := t.TempDir()

	// version -> [go, license], deliberately out of lexical order to assert
	// semver sorting.
	versions := map[string][2]string{
		"0.3.1":  {"1.17", "BSD-3-Clause"},
		"0.10.0": {"1.25.0", "MIT"},
		"0.9.0":  {"1.24.0", "MIT"},
	}
	for version, fields := range versions {
		content := fmt.Sprintf(fakeManifest, version, fields[0], fields[1])
		require.NoError(t, os.WriteFile(filepath.Join(dir, version+".nix"), []byte(content), 0o644))
	}

	require.NoError(t, writeIndex(dir))

	got, err := os.ReadFile(filepath.Join(dir, "index.nix"))
	require.NoError(t, err)

	golden.Assert(t, string(got), "index.nix.golden")
}

func TestWriteIndexMissingLicense(t *testing.T) {
	dir := t.TempDir()

	content := fmt.Sprintf(olderFakeManifest, "1.0.0", "1.18")
	require.NoError(t, os.WriteFile(filepath.Join(dir, "1.0.0.nix"), []byte(content), 0o644))

	require.NoError(t, writeIndex(dir))

	got, err := os.ReadFile(filepath.Join(dir, "index.nix"))
	require.NoError(t, err)

	golden.Assert(t, string(got), "index-missing-license.nix.golden")
}
