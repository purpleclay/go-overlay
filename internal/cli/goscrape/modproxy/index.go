package modproxy

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"slices"
	"strings"
	"text/template"

	"golang.org/x/mod/semver"
)

const toolIndexTemplate = `# Auto-generated by goscrape - do not edit manually
{
{{- range . }}
  "{{ .Version }}" = {go = "{{ .Go }}";};
{{- end }}
}
`

var indexTemplate = template.Must(template.New("tool-index").Parse(toolIndexTemplate))

// topLevelGoRe matches the top-level `go = "...";` field of a tool manifest.
// Manifests also have a `go` field per entry under `mod`, but those are
// indented further and so don't match this anchored 2-space pattern.
var topLevelGoRe = regexp.MustCompile(`(?m)^  go = "([^"]*)";$`)

type toolIndexEntry struct {
	Version string
	Go      string
}

func writeIndex(dir string) error {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return fmt.Errorf("failed to read manifest directory: %w", err)
	}

	var index []toolIndexEntry
	for _, e := range entries {
		name := e.Name()
		if e.IsDir() || !strings.HasSuffix(name, ".nix") || name == "index.nix" {
			continue
		}

		content, err := os.ReadFile(filepath.Join(dir, name))
		if err != nil {
			return fmt.Errorf("failed to read manifest %s: %w", name, err)
		}

		match := topLevelGoRe.FindStringSubmatch(string(content))
		if match == nil {
			return fmt.Errorf("manifest %s has no top-level go requirement", name)
		}

		index = append(index, toolIndexEntry{
			Version: strings.TrimSuffix(name, ".nix"),
			Go:      match[1],
		})
	}

	slices.SortFunc(index, func(a, b toolIndexEntry) int {
		return semver.Compare("v"+b.Version, "v"+a.Version)
	})

	var buf bytes.Buffer
	if err := indexTemplate.Execute(&buf, index); err != nil {
		return fmt.Errorf("failed to generate index: %w", err)
	}

	return os.WriteFile(filepath.Join(dir, "index.nix"), buf.Bytes(), 0o644)
}
